namespace BlazorBootstrap;

/// <summary>
/// Defines the size of an <see cref="Placeholder" /> element.
/// </summary>
public enum PlaceholderSize
{
    /// <summary>
    /// Don't resize an element.
    /// </summary>
    None,

    /// <summary>
    /// Makes an element extra small size.
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// Makes an element small size.
    /// </summary>
    Small,

    /// <summary>
    /// Makes an element large.
    /// </summary>
    Large
}
