namespace BlazorBootstrap;

/// <summary>
/// Defines the size of an <see cref="ConfirmDialog"/> element.
/// </summary>
public enum DialogSize
{
    /// <summary>
    /// Default ConfirmDialog width 500px will be applied.
    /// </summary>
    Regular,

    /// <summary>
    /// ConfirmDialog width 300px will be applied.
    /// </summary>
    Small,

    /// <summary>
    /// ConfirmDialog width 800px will be applied.
    /// </summary>
    Large,

    /// <summary>
    /// ConfirmDialog width 1140px will be applied.
    /// </summary>
    ExtraLarge
}
