namespace BlazorBootstrap;

public enum Unit
{
    /// <summary>
    /// No unit.
    /// </summary>
    None,

    /// <summary>
    /// Ems.
    /// </summary>
    Em,

    /// <summary>
    /// Percent.
    /// </summary>
    Percentage,

    /// <summary>
    /// Points.
    /// </summary>
    Pt,

    /// <summary>
    /// Pixels.
    /// </summary>
    Px,

    /// <summary>
    /// Root ems.
    /// </summary>
    Rem,

    /// <summary>
    /// Viewport height.
    /// </summary>
    Vh,

    /// <summary>
    /// Viewport maximum height.
    /// </summary>
    VMax,

    /// <summary>
    /// Viewport minimum height.
    /// </summary>
    VMin,

    /// <summary>
    /// Viewport width.
    /// </summary>
    Vw
}
