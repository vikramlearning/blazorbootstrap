namespace BlazorBootstrap;

/// <summary>
/// Represents a unit of measurement for CSS properties.
/// </summary>
public enum Unit
{
    /// <summary>
    /// No unit.
    /// </summary>
    None,

    /// <summary>
    /// Ems.
    /// </summary>
    em,

    /// <summary>
    /// Points.
    /// </summary>
    pt,

    /// <summary>
    /// Pixels.
    /// </summary>
    px,

    /// <summary>
    /// Root ems.
    /// </summary>
    rem,

    /// <summary>
    /// Viewport height.
    /// </summary>
    vh,

    /// <summary>
    /// Viewport maximum height.
    /// </summary>
    vmax,

    /// <summary>
    /// Viewport minimum height.
    /// </summary>
    vmin,

    /// <summary>
    /// Viewport width.
    /// </summary>
    vw,
}
