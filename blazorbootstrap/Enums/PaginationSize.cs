namespace BlazorBootstrap;

/// <summary>
/// Defines an element size.
/// </summary>
public enum PaginationSize
{
    /// <summary>
    /// Don't resize an element.
    /// </summary>
    None,

    /// <summary>
    /// Makes an element small size.
    /// </summary>
    Small,

    /// <summary>
    /// Makes an element large.
    /// </summary>
    Large
}