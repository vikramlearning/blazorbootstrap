namespace BlazorBootstrap;

/// <summary>
/// Defines the supported heading levels used by BlazorBootstrap components that render HTML heading elements.
/// </summary>
public enum HeadingSize
{
    /// <summary>
    /// Uses the `<h1>` heading level.
    /// </summary>
    [AddedVersion("1.10.0")]
    [Description("Uses the `h1` heading level.")]
    H1,

    /// <summary>
    /// Uses the `<h2>` heading level.
    /// </summary>
    [AddedVersion("1.10.0")]
    [Description("Uses the `h2` heading level.")]
    H2,

    /// <summary>
    /// Uses the `<h3>` heading level.
    /// </summary>
    [AddedVersion("1.10.0")]
    [Description("Uses the `h3` heading level.")]
    H3,

    /// <summary>
    /// Uses the `<h4>` heading level.
    /// </summary>
    [AddedVersion("1.10.0")]
    [Description("Uses the `h4` heading level.")]
    H4,

    /// <summary>
    /// Uses the `<h5>` heading level.
    /// </summary>
    [AddedVersion("1.10.0")]
    [Description("Uses the `h5` heading level.")]
    H5,

    /// <summary>
    /// Uses the `<h6>` heading level.
    /// </summary>
    [AddedVersion("1.10.0")]
    [Description("Uses the `h6` heading level.")]
    H6
}
