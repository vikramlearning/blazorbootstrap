namespace BlazorBootstrap;

/// <summary>
/// Predefined set of contextual colors.
/// </summary>
public enum CalloutColor
{
    /// <summary>
    /// No color will be applied to an element.
    /// </summary>
    None = 0,

    /// <summary>
    /// Danger color.
    /// </summary>
    Danger,

    /// <summary>
    /// Warning color.
    /// </summary>
    Warning,

    /// <summary>
    /// Info color.
    /// </summary>
    Info
}
